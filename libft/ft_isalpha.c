#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	elem;

	elem = c;
	if ((elem >= 'a' && elem <= 'z') || (elem >= 'A' && elem <= 'Z'))
		return (1);
	return (0);
}
